package devpaul.business.empresaexample.modelo;

public class Pasteleria {

    private String titulo;
    private String imagen;
    private String descripcion;
    private String precio;
    private String masdescripcion;

    public Pasteleria() {
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getImagen() {
        return imagen;
    }

    public void setImagen(String imagen) {
        this.imagen = imagen;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getPrecio() {
        return precio;
    }

    public void setPrecio(String precio) {
        this.precio = precio;
    }

    public String getMasdescripcion() {
        return masdescripcion;
    }

    public void setMasdescripcion(String masdescripcion) {
        this.masdescripcion = masdescripcion;
    }
}
